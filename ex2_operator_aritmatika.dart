void main(List<String> args) {
  int a = 10, b = 3; // Mendeklarasikan variabel a dan b sebagai integer dan menginisialisasinya dengan nilai 10 dan 3
  double c = 10.0; // Mendeklarasikan variabel c sebagai double dan menginisialisasinya dengan nilai 10.0
  
  // Menampilkan hasil operasi aritmatika antara a dan b menggunakan operator aritmatika
  print('\n$a + $b = ${a + b}'); // Penambahan
  print('$a - $b = ${a - b}'); // Pengurangan
  print('$a * $b = ${a * b}'); // Perkalian
  print('$a / $b = ${a / b}'); // Pembagian
  print('$a ~/ $b = ${a ~/ b}'); // Pembagian bulat
  print('$a % $b = ${a % b}'); // Modulus (sisa pembagian)
}
