void main(List<String> args) {
  int a = 9, b = 10; // Mendeklarasikan variabel a dan b sebagai integer dan menginisialisasinya dengan nilai 9 dan 10
  // Menampilkan hasil evaluasi dari operator relasional antara a dan b
  print('$a == $b: ${a == b}'); // Memeriksa apakah a sama dengan b
  print('$a != $b: ${a != b}'); // Memeriksa apakah a tidak sama dengan b
  print('$a > $b: ${a > b}');   // Memeriksa apakah a lebih besar dari b
  print('$a >= $b: ${a >= b}'); // Memeriksa apakah a lebih besar dari atau sama dengan b
  print('$a < $b: ${a < b}');   // Memeriksa apakah a lebih kecil dari b
  print('$a <= $b: ${a <= b}'); // Memeriksa apakah a lebih kecil dari atau sama dengan b
}
